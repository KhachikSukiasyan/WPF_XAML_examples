        private void Button_Click(object sender, RoutedEventArgs e)
        {
            if (!string.IsNullOrEmpty(loginTextBox.Text) && IsValidMail(loginTextBox.Text) && !string.IsNullOrEmpty(passwordTextBox.Password))
            {

                string name = loginTextBox.Text.Split('@')[0];
                MessageBox.Show(string.Format("Welcome {0} !", name) );
            }
            else
                MessageBox.Show("Incorrect mail or password !");

        }

        private bool IsValidMail(string emailaddress)
        {
            try
            {
                MailAddress m = new MailAddress(emailaddress);

                return true;
            }
            catch (FormatException)
            {
                return false;
            }
        }
